import java.math.BigDecimal;

public class ChiTietHoaDon {
    private int maHoaDon;
    private int maSanPham;
    private String tenSanPham;
    private int soLuong;
    private BigDecimal donGia;
    private BigDecimal giamGia;
    private int maLo;

    public ChiTietHoaDon() {}

    public ChiTietHoaDon(int maHoaDon, int maSanPham, int soLuong, BigDecimal donGia, BigDecimal giamGia,int maLo) {
        this.maHoaDon = maHoaDon;
        this.maSanPham = maSanPham;
        this.soLuong = soLuong;
        this.donGia = donGia;
        this.giamGia = giamGia;
        this.maLo= maLo;
    }

    public ChiTietHoaDon(int maHoaDon, int maSanPham, String tenSanPham, int soLuong, BigDecimal donGia, BigDecimal giamGia,int maLo) {
        this.maHoaDon = maHoaDon;
        this.maSanPham = maSanPham;
        this.tenSanPham = tenSanPham;
        this.soLuong = soLuong;
        this.donGia = donGia;
        this.giamGia = giamGia;
        this.maLo=maLo;
    }

    public int getMaLo() {
        return maLo;
    }

    public void setMaLo(int maLo) {
        this.maLo = maLo;
    }
    

    public String getTenSanPham() {
        return tenSanPham;
    }

    public void setTenSanPham(String tenSanPham) {
        this.tenSanPham = tenSanPham;
    }
    
    public int getMaHoaDon() {
        return maHoaDon;
    }

    public void setMaHoaDon(int maHoaDon) {
        this.maHoaDon = maHoaDon;
    }

    public int getMaSanPham() {
        return maSanPham;
    }

    public void setMaSanPham(int maSanPham) {
        this.maSanPham = maSanPham;
    }

    public int getSoLuong() {
        return soLuong;
    }

    public void setSoLuong(int soLuong) {
        this.soLuong = soLuong;
    }

    public BigDecimal getDonGia() {
        return donGia;
    }

    public void setDonGia(BigDecimal donGia) {
        this.donGia = donGia;
    }

    public BigDecimal getGiamGia() {
        return giamGia;
    }

    public void setGiamGia(BigDecimal giamGia) {
        this.giamGia = giamGia;
    }
}
